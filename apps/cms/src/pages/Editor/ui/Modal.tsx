import { ReactNode, useEffect, useRef } from 'react';
import { createPortal } from 'react-dom';
import { CloseButton, Group, ScrollArea, Stack, Text } from '@mantine/core';

import './Modal.css';

function PortalImpl({
  onClose,
  children,
  title,
  closeOnClickOutside,
}: {
  children: ReactNode;
  closeOnClickOutside: boolean;
  onClose: () => void;
  title: string;
}) {
  const modalRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (modalRef.current !== null) {
      modalRef.current.focus();
    }
  }, []);

  useEffect(() => {
    let modalOverlayElement: HTMLElement | null = null;
    const handler = (event: KeyboardEvent) => {
      if (event.key === 'Escape') {
        onClose();
      }
    };
    const clickOutsideHandler = (event: MouseEvent) => {
      const target = event.target;
      if (
        modalRef.current !== null &&
        !modalRef.current.contains(target as Node) &&
        closeOnClickOutside
      ) {
        onClose();
      }
    };
    const modelElement = modalRef.current;
    if (modelElement !== null) {
      modalOverlayElement = modelElement.parentElement;
      if (modalOverlayElement !== null) {
        modalOverlayElement.addEventListener('click', clickOutsideHandler);
      }
    }

    window.addEventListener('keydown', handler);

    return () => {
      window.removeEventListener('keydown', handler);
      if (modalOverlayElement !== null) {
        modalOverlayElement?.removeEventListener('click', clickOutsideHandler);
      }
    };
  }, [closeOnClickOutside, onClose]);

  return (
    <div className='Modal__overlay' role='dialog'>
      <div className='Modal__modal' tabIndex={-1} ref={modalRef}>
        <Group className='Modal__modalHeader' justify='space-between'>
          <Text>{title}</Text>
          <CloseButton className='Modal__closeButton' onClick={onClose} />
        </Group>
        <ScrollArea
          classNames={{
            root: 'Modal__scrollareaRoot',
            thumb: 'Modal__scrollbarThumb',
            viewport: 'Modal__scrollareaViewport',
          }}
          scrollbars='y'
          scrollbarSize={'0.5rem'}
        >
          <Stack className='Modal__content'>{children}</Stack>
        </ScrollArea>
      </div>
    </div>
  );
}

export default function Modal({
  onClose,
  children,
  title,
  closeOnClickOutside = false,
}: {
  children: ReactNode;
  closeOnClickOutside?: boolean;
  onClose: () => void;
  title: string;
}): JSX.Element {
  return createPortal(
    <PortalImpl
      onClose={onClose}
      title={title}
      closeOnClickOutside={closeOnClickOutside}
    >
      {children}
    </PortalImpl>,
    document.body
  );
}
